<div class="pregunta"><a name="2">Funcion Mantener PTC.</a> <a href="#up"><img src="/files/images/otros/arrow-up.gif" /></a></div>
<p class="respuesta">La funcion permite revisar, como siempre de un s&oacute;lo click, las siguientes PTC`S:</p>
	<li>Las que tenemos en papelera (esto podemos configurarlo desde nuestro perfil marcando la opci&oacute;n remarcada en la captura de abajo):
<div class="imagen"><img src="/files/images/content/PasoII3[2].png" /></div>
<p class="respuesta">si adem&aacute;s tenemos marcado el "activar autopapelera" con esta funci&oacute;n podremos tambi&eacute;n revisar:</p>
	<li>Las que son antiscam en espera de primer pago (ya se podr&aacute; marcar el pago pedido sin miedo a que las dejemos inactivas hasta que cobremos</li>
	<li>Las que tienen un n&uacute;mero de ads/d&iacute;a menor que el que indicamos en el criterio de nuestra autopapelera </li>

<p class="respuesta">Obviamente, si no tenemos marcada ninguna de las opciones tanto de "incluir papelera para mantener" como "activar autopapelera", nos dar&aacute; un mensaje diciendo que "no hay ninguna ptc para mantener" por tanto.</p>
<p class="respuesta">La revisi&oacute;n o mantenimiento (de ah&iacute; que el bot&oacute;n se llame "mantener") consiste en mostrar nuestra actividad cada ciertos d&iacute;as (yo recomendar&iacute;a cada semana aproximadamente), de forma que as&iacute; ninguna ptc nos suspenda la cuenta por inactividad. Para ello lo que har&iacute;amos es loguearnos en cada una y clickear por ejemplo un s&oacute;lo ad respectivamente, todo con tal de mostrar algo de actividad por lo que no vamos a darle a full como con las dem&aacute;s ni mucho menos, es decir, que en unos 5 minutos ya deber&iacute;amos de haber acabado de hacerlo todo (seg&uacute;n tambi&eacute;n la pr&aacute;ctica que tengan ya con esto, claro est&aacute;.</p>
<p class="respuesta"><b> Importante :</b> Antes de ejecutar esta funci&oacute;n siempre acu&eacute;rdense de configurar el navegador en modo "abrir enlace en nueva ventana", al igual que hac&iacute;amos con "clickear" en las tandas. </p> 
<p class="respuesta">Luego cuando hayan terminado pulsan el bot&oacute;n de "mantenimiento terminado" que aparece:</p>
<div class="imagen"><img src="/files/images/content/PasoII3[3].png" /></div>
<p class="respuesta">Al igual como ocurr&iacute;a con la funci&oacute;n "clickear" se nos muestra el tiempo que pas&oacute; desde que lo ejecutamos, en este caso en d&iacute;as como ven entre los corchetes al lado de dicho bot&oacute;n para que as&iacute; siempre puedan tenerlo bien controlado.</p>
