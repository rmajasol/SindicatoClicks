<div class="pregunta"><a name="2">Cobrar en AlertPay.</a> <a href="#up"><img src="/files/images/otros/arrow-up.gif" /></a></div>
<p class="respuesta">Bueno primero que nada vale aclarar que este tutorial esta realizado en base a comentarios de diferentes usuarios por Internet y por la información que brinda Alertpay en su web. Aclarado el asunto, podemos decir que existen pocas formas de retirar el dinero en Sudamerica directamente de Alertpay.</p>
<p class="respuesta">Aquí nombramos 3 formas de retirar el dinero de nuestras cuentas de Alertpay:</p>
	<li> Transferencia bancaria. </li>
<p class="respuesta">En cuanto a ésta opción hay un listado que da AlertPay de Bancos de distintos países dentro de los cuales, el único país de habla hispana que se encuentra es España.
Una opción que se podria probar seria abrir una cuenta en un Banco de España, por ejemplo el banco de Galicia el cual está lleno de sucursales acá en Argentina, y luego transferir el dinero desde nuestra cuenta bancaria.</p>
	<li> Cheque. </li>
    <p class="respuesta">La segunda es la adecuada si contamos con una sucursal de Banco Piano en nuestra región, ya que es el único banco que acepta cheques internacionales. Además es barato, ya que Alertpay nos cobra sólo U$S 2 la transacción.</p>
<p class="respuesta">Aquí esta la lista de Fees que cobra Alertpay para las diferentes transacciones.</p>
<div class="imagen"><img src="/files/images/content/PasoIV1[9].jpg" /></div>
<p class="respuesta">Aquí esta la lista de Fees que cobra Alertpay para las diferentes transacciones.</p>
<p class="respuesta">Esta opción es la más complicada ya que genera cierto riesgo de estafa pero quizás a su vez también sea la mas segura para obtener dinero en mano (siempre y cuando se intercambie con alguien de confianza).</p>
<p class="respuesta">Para realizar este método necesitaríamos además de la cuenta de Alertpay, una cuenta en otro de los sistemas de transferencia de dinero en Internet (Paypal, E-gold, Moneybookers,etc).Como asi también una persona que necesite fondos en Alertpay.</p>
<p class="respuesta">El intercambio se hace del siguiente modo(a modo de ejemplo haremos un intercambio con una cuenta de paypal ) :</p>
<p class="respuesta">Tenemos una persona A que es la interesada en retirar el dinero acumulado en alertpay.</p>
<p class="respuesta">La persona A con fondos en Alertpay envía un X monto a la cuenta alertpay de una persona B. Esta persona B que posee fondos en Paypal envía sus fondos a la cuenta de Paypal de la persona A.</p>
<p class="respuesta">Allí termina el intercambio entre usuarios.</p>
