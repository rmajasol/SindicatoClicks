<div class="pregunta"><a name="1">Funcion de la Papelera.</a> <a href="#up"><img src="/files/images/otros/arrow-up.gif" /></a></div>
<p class="respuesta">La Papelera es una funcion del cual la funcion que realiza es dejar de clickera alguna PTC, por ejemplo: Si no quiero hacer clicks en una PTC por que no me gusta su disminucion de ads o cualquier otro motivo, lo que tienes que hacer es a&ntilde;adir esa PTC a papelera. Para a&ntilde;adirla tienes que ingresar a la Papelera <a href="http://www.sindicatoclicks.net/index.php?mod=papelera"target="_blank">aqui</a>.</p>
<p class="respuesta">Luego hacer click en <b>" Insertar Empresa "</b>.Luego seleccionas la PTC que quieres a&ntilde;adir y haces click en <b> " Insertar " </b>:</p>
<p class="respuesta">Y listo ya tienes la PTC a&ntilde;adida en la Papelera. Tambien puedes quitar la PTC de la Papelera haciendo click en <b> " Quitar Empresa " </b>. Luego seleccionas la PTC que quieras volver a la lista de PTC para clickear y haces click en <b> " Quitar " </b>.</p>
<p class="respuesta">Tienes una funcion Automatica de la Papelera que asigna de forma automatica las empresas que estan con poco ads o estan esperando algun pago , es decir aquellas anti-scam esperando el pago. </p>
